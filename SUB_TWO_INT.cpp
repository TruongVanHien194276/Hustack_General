// Problem: Subtract 2 integers
// Description
// Given 2 integers a and b. Compute a - b.
// Input
// Line 1: contains a and b (1 <= a,b <= 10000)

// Output
// Write a - b

// Example
// Input
// 3 5
// Output
// -2

#include <bits/stdc++.h>

using namespace std;

int main(int argc, const char** argv) {
    int a, b;
    cin >> a >> b;
    cout << a - b;
    return 0;
}